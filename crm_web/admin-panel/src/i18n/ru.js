export function stageLabel(stage) {
  if (!stage) return RU.messages.notSet;
  return RU.stages[stage] || stage;
}

export function formatCurrencyRub(value) {
  const amount = Number(value);
  if (!Number.isFinite(amount)) return RU.messages.notSet;

  return new Intl.NumberFormat("ru-RU", {
    style: "currency",
    currency: "RUB",
    currencyDisplay: "narrowSymbol",
    maximumFractionDigits: 0,
  }).format(amount);
}

export function formatDateRu(value, options) {
  if (!value) return RU.messages.notSet;

  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return String(value);

  return new Intl.DateTimeFormat("ru-RU", options).format(date);
}

export function withTitle(template, title) {
  return template.replace("{title}", title);
}

